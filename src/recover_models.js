const processedModels = [];

for (const [brand, csv] of Object.entries(rawData)) {
    const models = csv.trim().split('\n').slice(1)
        .flatMap(line => line.split(',').map(item => item.trim().toUpperCase()))
        .filter(item => item && !item.toLowerCase().includes('modelo'))
        .map(item => ({ brand, name: item, in_stock: true }));

    processedModels.push(...models);
}

const values = processedModels.map(m => `('${m.brand.replace(/'/g, "''")}', '${m.name.replace(/'/g, "''")}', ${m.in_stock})`).join(',\n');


